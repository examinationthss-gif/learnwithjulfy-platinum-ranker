// Character-by-character mapper to catch raw patterns safely
function repairAssameseText(text) {
  if (!text) return text;
  let t = text;

  // 1. First, replace known exact words from our repair dictionary
  // We split by spaces/punctuation to keep words intact
  const tokens = t.split(/(\s+|[,?.!()|:;/"'—\-–━\n])/);
  const repairedTokens = tokens.map(tok => {
    // Strip trailing/leading symbols to match dictionary keys
    const clean = tok.replace(/[.,\/#!$%\^&\*;:{}=\-_`~()]/g, "");
    if (repairMap[tok]) {
      return repairMap[tok];
    }
    if (repairMap[clean]) {
      return tok.replace(clean, repairMap[clean]);
    }
    return tok;
  });
  t = repairedTokens.join("");

  // 2. Visual vowel-sign 'ি' represented by visual order shifts (e.g. prefix 'ম' before consonants)
  const consonants = "কখগঘঙচছজঝঞটঠডঢণত্থদধনপফবভমযৰলৱশষসহড়ঢ়য়";
  
  // Replace: ম + Consonant -> Consonant + ি
  // We do NOT replace if it forms a common valid word like 'মৰম' or 'মত' or 'মন' or 'মৰ' or 'মদ'
  const regexM = new RegExp(`ম([${consonants}])`, "g");
  t = t.replace(regexM, (match, p1) => {
    // Exclude certain valid syllables
    const excluded = ["ন", "ত", "দ", "ৰ", "ল"];
    if (excluded.includes(p1) && (t.includes("ম" + p1 + " ") || t.includes("ম" + p1 + "।") || t.includes("ম" + p1 + "ৰ"))) {
      return match; // keep as is
    }
    return p1 + "ি";
  });

  // 3. Spacing and glyph anomalies
  t = t.replace(/য\s+া/g, "য়া");
  t = t.replace(/ল\s+া/g, "লা");
  t = t.replace(/দ\s+া/g, "দা");
  t = t.replace(/ত\s+া/g, "তা");
  t = t.replace(/ম\s+া/g, "ma"); // temporary
  t = t.replace(/ন\s+া/g, "না");
  t = t.replace(/ma/g, "মা");
  t = t.replace(/া\s+ৰ/g, "াৰ");
  t = t.replace(/ৈ\s+মছল/g, "ছিল");
  t = t.replace(/ৈ\s+ল/g, "ছিল");

  // 4. Double check specific residual corruptions
  t = t.replace(/মিক্ষা/g, "শিক্ষা");
  t = t.replace(/বিক্ষা/g, "শিক্ষা");
  t = t.replace(/আয াগ/g, "আয়োগ");
  t = t.replace(/সভাপমত/g, "সভাপতি");
  t = t.replace(/আমছল/g, "আছিল");
  t = t.replace(/মনযুক্ত/g, "নিযুক্ত");
  t = t.replace(/কমৰমছল/g, "কৰিছিল");
  t = t.replace(/বছৰত/g, "বছৰত"); // ensure safety
  t = t.replace(/বাবে/g, "বাবে"); // ensure safety
  t = t.replace(/বৃমি/g, "বৃত্তি");
  t = t.replace(/মুদামল াৰ/g, "মুডালিয়াৰ");
  t = t.replace(/েযৰ/g, "কৰে");
  t = t.replace(/হহবিল/g, "হৈছিল");
  t = t.replace(/িযৱহাবৰে/g, "ব্যৱহাৰিক");
  t = t.replace(/বিক্ষাৰ্থী/g, "শিক্ষাৰ্থী");
  t = t.replace(/বিোি/g, "বিকাশ");

  return t;
}

// Repair all MCQs
const repaired = data.map(m => {
  return {
    ...m,
    asQuestion: repairAssameseText(m.asQuestion),
    asOptions: m.asOptions.map(opt => repairAssameseText(opt)),
    asExplanation: repairAssameseText(m.asExplanation)
  };
});

fs.writeFileSync(path.join(__dirname, "../extracted_mcqs.json"), JSON.stringify(repaired, null, 2), "utf8");
