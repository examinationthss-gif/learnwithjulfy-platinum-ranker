function improveText(text) {
  if (!text) return text;
  let t = text;
  
  // Apply replacements iteratively
  for (const [pattern, replacement] of replacements) {
    t = t.replace(pattern, replacement);
  }
  
  // Custom final polish to guarantee no broken characters or orphan spaces
  t = t.replace(/\s+/g, " ");
  t = t.trim();
  
  return t;
}

let modifiedCount = 0;
const modifiedIds = [];

const updatedData = data.map(m => {
  const oldQ = m.asQuestion;
  const oldOpts = JSON.stringify(m.asOptions);
  const oldExp = m.asExplanation;
  
  const newQ = improveText(m.asQuestion);
  const newOpts = m.asOptions.map(opt => improveText(opt));
  const newExp = improveText(m.asExplanation);
  
  if (oldQ !== newQ || oldOpts !== JSON.stringify(newOpts) || oldExp !== newExp) {
    modifiedCount++;
    modifiedIds.push(m.mcqId);
  }
  
  return {
    ...m,
    asQuestion: newQ,
    asOptions: newOpts,
    asExplanation: newExp
  };
});

